package com.technawabs.openhouz.models;

public class MetroStation {

    private String metroStationName;
    private String distanceFromMetroStation;

    public String getMetroStationName() {
        return metroStationName;
    }

    public void setMetroStationName(String metroStationName) {
        this.metroStationName = metroStationName;
    }

    public String getDistanceFromMetroStation() {
        return distanceFromMetroStation;
    }

    public void setDistanceFromMetroStation(String distanceFromMetroStation) {
        this.distanceFromMetroStation = distanceFromMetroStation;
    }
}
